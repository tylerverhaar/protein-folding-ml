def derive_residue_atom_id_mapping(fpath: str = 'feature_engineering/mapping.txt'):
	mapping = {}

	f = open(fpath, 'r')
	flines = [l.strip('\n').split(' ') for l in f.readlines()]
		
	for ln in flines:
		res = ln[0]
		mapping[res] = {}
		ln = ln[2:]; n = len(ln)
		idx = 0
		while True:
			ln_idx_d0 = ln[idx]
			ln_idx_d1 = int(ln[idx+1])
			mapping[res][ln_idx_d0] = ln_idx_d1
			if idx + 3 > n - 1:
				break
			else:
				idx += 3
	return mapping

